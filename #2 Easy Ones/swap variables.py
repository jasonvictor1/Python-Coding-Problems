# Initialize two variables 'a' and 'b' with values 6 and 7, respectively
a = 6
b = 7

# Swapping the values of 'a' and 'b'
# This is done in a pythonic way where 'a' gets the value of 'b' and 'b' gets the value of 'a'
a, b = b, a

# Print the values of 'a' and 'b' after swapping
print("a =", a)
print("b =", b)
