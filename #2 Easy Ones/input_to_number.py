# Prompt the user for an integer number and store the input as an integer
int_num = int(input('Give me an integer number: '))

# Prompt the user for a floating point number and store the input as a float
float_num = float(input('Give me a float  number: '))

# Multiply the integer number with the floating point number
result = int_num * float_num

# Display the result of the multiplication
print('Your result is: ', result)
